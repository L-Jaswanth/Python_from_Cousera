# Getting a list item

letters = ["a", "b", "c", "d", "e"]
print(letters)

# counting starts from 0 and -1 from ending in python
# ["a","b","c","d","e"]
#   0   1   2   3   4
#   -5  -4  -3 -2  -1

print(letters[0])  # 1st element
# print(letters[6]) gives list index out of range error

print(letters[-1])  # refers to last element in list
