''' 
    The 'in' keyword can also be used to check to see if one string is
    'in' another string
'''
'''
    The 'in' expression is a logical expression that returns True or False
    and can be used in an if statement
'''

fruit= 'banana'
print('n' in fruit)#True
print('m' in fruit) #False
print('nan' in fruit) #True

if 'a' in fruit:
    print('Found it!')
